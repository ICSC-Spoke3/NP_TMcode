      TQCS(IPO,2)=-DREAL((CTQCS(IPO,1)+CTQCS(IPO,3))*UIM)*SQ2I
      TQCS(IPO,3)=-DREAL(CTQCS(IPO,2))
      C1=TQCPS(IPO,1)
      C2=TQCPS(IPO,2)
      C3=TQCPS(IPO,3)
      TQCPS(IPO,1)=-(C1-C3)*SQ2I
      TQCPS(IPO,2)=-(C1+C3)*(UIM*SQ2I)
      TQCPS(IPO,3)=-C2
   78 CONTINUE
      RETURN
      END
      SUBROUTINE RFTR
     1(U,UP,UN,GAPV,EXTINS,SCATTS,RAPR,COSAV,FP,FN,FK,FX,FY,FZ)
